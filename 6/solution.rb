# Part 2

fishes256 = 256.times.reduce(fishes.tally) do |memo|
  memo.each_with_object(Hash.new { |h, k| h[k] = 0 }) do |(n, c), new_fishes|
    if (n - 1) >= 0
      new_fishes[(n - 1)] += c
    else
      new_fishes[6] += c
      new_fishes[8] += c
    end
  end
end
