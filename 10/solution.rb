lines = DATA.each_line.map { _1.strip.each_char.to_a }

PAIRS = { '(' => ')', '[' => ']', '{' => '}',  '<' => '>' }
SYNTAX_ERROR_SCORES = { ')' => 3,  ']' => 57,  '}' => 1197, '>' => 25137 }
COMPLETION_SCORE = { ')' => 1,  ']' => 2,  '}' => 3, '>' => 4 }

def check(line)
  stack = []

  line.each do |char|
    case char
    when *PAIRS.keys
      stack.push(char)
    when *PAIRS.values
      opener = stack.pop
      return [SYNTAX_ERROR_SCORES.fetch(char), 0] unless PAIRS.fetch(opener) == char
    else
      raise "Unexpected char: #{char}"
    end
  end

  completion_score = stack.reverse.inject(0) do |memo, opener|
    memo *= 5
    memo += COMPLETION_SCORE.fetch(PAIRS.fetch(opener))
  end

  [0, completion_score]
end

# Part 1

syntax_error_score = lines.map { score, _ = check(_1); score }.sum
puts "Syntax error score: #{syntax_error_score}"

# Part 2

completion_scores = lines.map { _, score = check(_1); score }.reject(&:zero?)
completion_score = completion_scores.sort[completion_scores.length / 2]
puts "Completion score: #{completion_score}"

